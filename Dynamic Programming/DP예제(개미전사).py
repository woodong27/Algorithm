'''
일직선으로 이어진 식량창고에 각각 다른 수의 식량이 저장되어 있음
습격한 창고의 바로 양 옆을 공격하면 들키기 때문에 한번 습격한 후에는
최소 한칸이상 떨어진 창고를 약탈해야 함
첫째줄에 식량창고의 갯수 N(3<=N<=100)
둘째줄에 공백으로 각 창고에 저장된 식량의 갯수 K(0<=K<=1000)가 주어짐
얻을 수 있는 식량의 최대값을 구하는 프로그램을 작성할 것
'''
N=int(input())
meal=[0]+list(map(int,input().split()))

dp=[0]*101

dp[1]=meal[1]
dp[2]=meal[2]

'''
i번째 창고를 털어도 될지 안될지를 결정하는 것은 i-1번째와 i-2번째를 털었는지
안털었는지의 유무
-> f(i)=max(f(i-1),f(i-2)+k(i))
'''

for i in range(3,N+1):
    dp[i]=max(dp[i-1],dp[i-2]+meal[i])

print(dp[N])